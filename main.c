#include "main.h"          // CubeMX 生成的头文件 (内含 stm32g4xx_hal.h)

/*==================================================================================*/
/*                     一、硬件相关层 (换引脚/换芯片只改这里)                        */
/*==================================================================================*/
#define OLED_SCL_GPIO_PORT   GPIOB
#define OLED_SCL_PIN         GPIO_PIN_8
#define OLED_SDA_GPIO_PORT   GPIOB
#define OLED_SDA_PIN         GPIO_PIN_9

#define OLED_SCL_HIGH()   HAL_GPIO_WritePin(OLED_SCL_GPIO_PORT, OLED_SCL_PIN, GPIO_PIN_SET)
#define OLED_SCL_LOW()    HAL_GPIO_WritePin(OLED_SCL_GPIO_PORT, OLED_SCL_PIN, GPIO_PIN_RESET)
#define OLED_SDA_HIGH()   HAL_GPIO_WritePin(OLED_SDA_GPIO_PORT, OLED_SDA_PIN, GPIO_PIN_SET)
#define OLED_SDA_LOW()    HAL_GPIO_WritePin(OLED_SDA_GPIO_PORT, OLED_SDA_PIN, GPIO_PIN_RESET)
#define OLED_SDA_READ()   HAL_GPIO_ReadPin(OLED_SDA_GPIO_PORT, OLED_SDA_PIN)

/* 微秒级延时: 软件 I2C 对时序不敏感, 简单空循环即可。
 * G474 主频高, 这里循环次数粗调; 不准也不影响 OLED 通信。 */
static void i2c_delay(void)
{
    volatile uint16_t i = 20;
    while (i--) { __NOP(); }
}

/* 如果在 CubeMX 里已经把 PB8/PB9 配成开漏输出, 这个函数可以不调用。
 * 为保证文件自包含, 这里也提供一份 GPIO 初始化。 */
static void MX_OLED_GPIO_Init(void)
{
    GPIO_InitTypeDef GPIO_InitStruct = {0};

    __HAL_RCC_GPIOB_CLK_ENABLE();          // 使能 GPIOB 时钟 (按你的端口改)

    HAL_GPIO_WritePin(OLED_SCL_GPIO_PORT, OLED_SCL_PIN, GPIO_PIN_SET);
    HAL_GPIO_WritePin(OLED_SDA_GPIO_PORT, OLED_SDA_PIN, GPIO_PIN_SET);

    GPIO_InitStruct.Pin   = OLED_SCL_PIN | OLED_SDA_PIN;
    GPIO_InitStruct.Mode  = GPIO_MODE_OUTPUT_OD;   // 开漏输出
    GPIO_InitStruct.Pull  = GPIO_PULLUP;           // 内部上拉(建议外部再加4.7k上拉)
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_HIGH;
    HAL_GPIO_Init(OLED_SCL_GPIO_PORT, &GPIO_InitStruct);
}

/*==================================================================================*/
/*                          二、软件模拟 I2C 时序                                   */
/*==================================================================================*/
static void I2C_Start(void)
{
    OLED_SDA_HIGH();
    OLED_SCL_HIGH();
    i2c_delay();
    OLED_SDA_LOW();          // SCL 高电平时 SDA 拉低 = 起始
    i2c_delay();
    OLED_SCL_LOW();
}

static void I2C_Stop(void)
{
    OLED_SCL_LOW();
    OLED_SDA_LOW();
    i2c_delay();
    OLED_SCL_HIGH();
    i2c_delay();
    OLED_SDA_HIGH();         // SCL 高电平时 SDA 拉高 = 停止
    i2c_delay();
}

/* 等待应答 (这里不强制校验, OLED 一般都会 ACK) */
static void I2C_WaitAck(void)
{
    OLED_SDA_HIGH();         // 释放 SDA 让从机拉低
    i2c_delay();
    OLED_SCL_HIGH();
    i2c_delay();
    (void)OLED_SDA_READ();   // 可在此读取 ACK 位做校验
    OLED_SCL_LOW();
    i2c_delay();
}

/* 发送一个字节 (高位在前) */
static void I2C_SendByte(uint8_t dat)
{
    uint8_t i;
    for (i = 0; i < 8; i++)
    {
        OLED_SCL_LOW();
        if (dat & 0x80) OLED_SDA_HIGH();
        else            OLED_SDA_LOW();
        dat <<= 1;
        i2c_delay();
        OLED_SCL_HIGH();     // 拉高时钟, 从机采样
        i2c_delay();
    }
    OLED_SCL_LOW();
}

/*==================================================================================*/
/*                          三、SSD1306 OLED 底层命令                               */
/*==================================================================================*/
#define OLED_ADDR     0x78        // 7位地址 0x3C 左移1位 = 0x78
#define OLED_CMD      0x00        // 控制字节: 写命令
#define OLED_DATA     0x40        // 控制字节: 写数据

static void OLED_WriteByte(uint8_t dat, uint8_t type)
{
    I2C_Start();
    I2C_SendByte(OLED_ADDR);   I2C_WaitAck();
    I2C_SendByte(type);        I2C_WaitAck();
    I2C_SendByte(dat);         I2C_WaitAck();
    I2C_Stop();
}

#define OLED_WR_CMD(c)   OLED_WriteByte((c), OLED_CMD)
#define OLED_WR_DATA(d)  OLED_WriteByte((d), OLED_DATA)

/* 设置显示坐标: 列 x(0~127), 页 y(0~7) */
static void OLED_SetPos(uint8_t x, uint8_t y)
{
    OLED_WR_CMD(0xB0 + y);
    OLED_WR_CMD(((x & 0xF0) >> 4) | 0x10);
    OLED_WR_CMD(x & 0x0F);
}

static void OLED_Fill(uint8_t data)
{
    uint8_t page, col;
    for (page = 0; page < 8; page++)
    {
        OLED_SetPos(0, page);
        for (col = 0; col < 128; col++)
            OLED_WR_DATA(data);
    }
}

void OLED_Clear(void) { OLED_Fill(0x00); }

void OLED_DisplayOn(void)  { OLED_WR_CMD(0x8D); OLED_WR_CMD(0x14); OLED_WR_CMD(0xAF); }
void OLED_DisplayOff(void) { OLED_WR_CMD(0x8D); OLED_WR_CMD(0x10); OLED_WR_CMD(0xAE); }

void OLED_Init(void)
{
    MX_OLED_GPIO_Init();           // 若已在 CubeMX 配好引脚, 可删掉此行
    HAL_Delay(100);                // 等待 OLED 上电稳定

    OLED_WR_CMD(0xAE);             // 关闭显示
    OLED_WR_CMD(0x20); OLED_WR_CMD(0x10);  // 页寻址模式
    OLED_WR_CMD(0xB0);             // 页起始地址
    OLED_WR_CMD(0xC8);             // COM 扫描方向(上下翻转)
    OLED_WR_CMD(0x00);             // 低列地址
    OLED_WR_CMD(0x10);             // 高列地址
    OLED_WR_CMD(0x40);             // 起始行地址
    OLED_WR_CMD(0x81); OLED_WR_CMD(0xCF);  // 对比度
    OLED_WR_CMD(0xA1);             // 段重定义(左右翻转)
    OLED_WR_CMD(0xA6);             // 正常显示(A7 反白)
    OLED_WR_CMD(0xA8); OLED_WR_CMD(0x3F);  // 多路复用比 1/64
    OLED_WR_CMD(0xA4);             // 显示跟随 RAM
    OLED_WR_CMD(0xD3); OLED_WR_CMD(0x00);  // 显示偏移
    OLED_WR_CMD(0xD5); OLED_WR_CMD(0x80);  // 时钟分频
    OLED_WR_CMD(0xD9); OLED_WR_CMD(0xF1);  // 预充电周期
    OLED_WR_CMD(0xDA); OLED_WR_CMD(0x12);  // COM 引脚配置
    OLED_WR_CMD(0xDB); OLED_WR_CMD(0x40);  // VCOMH
    OLED_WR_CMD(0x8D); OLED_WR_CMD(0x14);  // 开启电荷泵
    OLED_WR_CMD(0xAF);             // 开启显示
    OLED_Clear();
}

