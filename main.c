/* 显示一个 8x16 ASCII 字符. x:0~127  y(页):0/2/4/6 */
void OLED_ShowChar(uint8_t x, uint8_t y, char chr)
{
    uint8_t i, c;
    if (chr < ' ' || chr > '~') chr = ' ';
    c = chr - ' ';
    if (x > 120) { x = 0; y += 2; }

    OLED_SetPos(x, y);
    for (i = 0; i < 8; i++) OLED_WR_DATA(F8X16[c * 16 + i]);
    OLED_SetPos(x, y + 1);
    for (i = 0; i < 8; i++) OLED_WR_DATA(F8X16[c * 16 + i + 8]);
}

/* 显示 ASCII 字符串 */
void OLED_ShowString(uint8_t x, uint8_t y, const char *str)
{
    while (*str != '\0')
    {
        OLED_ShowChar(x, y, *str);
        x += 8;
        if (x > 120) { x = 0; y += 2; }
        str++;
    }
}

/* 按索引显示一个 16x16 汉字 */
void OLED_ShowCNByIndex(uint8_t x, uint8_t y, uint8_t index)
{
    uint8_t i;
    OLED_SetPos(x, y);
    for (i = 0; i < 16; i++) OLED_WR_DATA(CN16X16[index][i]);
    OLED_SetPos(x, y + 1);
    for (i = 0; i < 16; i++) OLED_WR_DATA(CN16X16[index][i + 16]);
}

/* 显示 UTF-8 中文字符串(可中英文混排). 汉字占 16 像素宽, y 取 0/2/4/6 */
void OLED_ShowChinese(uint8_t x, uint8_t y, const char *str)
{
    uint8_t i;
    while (*str)
    {
        if ((uint8_t)*str < 0x80)          // ASCII 字符
        {
            OLED_ShowChar(x, y, *str);
            x += 8; str += 1;
            continue;
        }
        for (i = 0; i < CN_COUNT; i++)     // UTF-8 汉字一般占 3 字节
        {
            const char *p = cn_index[i];
            if (p[0] == str[0] && p[1] == str[1] && p[2] == str[2])
            {
                OLED_ShowCNByIndex(x, y, i);
                break;
            }
        }
        x += 16; str += 3;
        if (x > 112) { x = 0; y += 2; }
    }
}

static uint32_t oled_pow(uint8_t m, uint8_t n)
{
    uint32_t result = 1;
    while (n--) result *= m;
    return result;
}

/* 显示无符号整数. len=显示位数(前导 0 显示为空格) */
void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len)
{
    uint8_t t, temp, enshow = 0;
    for (t = 0; t < len; t++)
    {
        temp = (num / oled_pow(10, len - t - 1)) % 10;
        if (enshow == 0 && t < (len - 1))
        {
            if (temp == 0) { OLED_ShowChar(x + 8 * t, y, ' '); continue; }
            else            enshow = 1;
        }
        OLED_ShowChar(x + 8 * t, y, temp + '0');
    }
}

